
#include "keygen.h"
#include <stdio.h>

void printhex(void * byteVoid, size_t size) {
	size_t i;
	uint8_t * bytes = (uint8_t *) byteVoid;
	for (i = 0; i < size; i++) {
		if ((i % 16) > 0) {
			printf(" ");
		}
		printf("%02X", bytes[i]);
		if ((i % 16) == 15) {
			printf("\n");
		}
	}
	if ((i % 16) != 15) {
		printf("\n");
	}
}

